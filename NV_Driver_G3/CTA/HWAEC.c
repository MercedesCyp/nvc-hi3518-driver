/*  static int32 sfdefFM1288_InitChip(void)
@introduction:
    配置初始设置

@parameter:
    void

@return:
    0   SUCCESS
    -1  FAIL
*/
static int32 sfdefFM1288_InitChip(void)
{
	int ret = -1;
	mFM1288_WriteData stWriteData;
	//mFM1288_ReadData stReadData;
	//int ReadData=0;

	// write init data
#if DEBUG_FM1288
	NVCPrint("FM1288 start write init data.");
#endif
	memset(&stWriteData, 0, sizeof(stWriteData));
	stWriteData.u8Data = gClassFM1288.apInitData->apData;
	if (gClassFM1288.apInitData->apDataLen == 0) {
		gClassFM1288.apInitData->apDataLen = sizeof(u8InitData);
	}
	stWriteData.u32DataLength = gClassFM1288.apInitData->apDataLen;
	ret = sfdefFM1288_WriteData(&stWriteData);
#if DEBUG_FM1288
	NVCPrint("FM1288 write init data OK");
#endif
	if (ret < 0) {
		return -1;
	}
#if DEBUG_INIT
	NVCPrint("The FM1288 module Start! OK");
#endif
	return 0;
}

