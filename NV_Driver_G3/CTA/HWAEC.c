/*  static void  sfdefFM1288_WriteData(mFM1288_WriteData* pWriteData)
@introduction:
    修改FM1288寄存器的值

@parameter:
    iCmd
        指向写入数据结构体的指针

@return:
    void

*/
static int32 sfdefFM1288_WriteData(mFM1288_WriteData *pWriteData)
{
	int i = 0;

	IIC_Start();
	while (i < pWriteData->u32DataLength) {
		//NVCPrint("Send InitData[%d]: 0x%02x", i, pWriteData->u8Data[i]);
#if (DEBUG_FM1288 & 0)
		if (0xFC == pWriteData->u8Data[i]) {
			printk("\n");
		}
		printk("0x%X  ", pWriteData->u8Data[i]);
#endif
		IIC_WriteChar(pWriteData->u8Data[i]);
		if (IIC_ACK(DC_IIC_DETECTACK)) {
			NVCPrint("[SendConfig]DETECTACK fail.");
			return -1;
		}
		i++;
	}
	IIC_Stop();

	return 0;
}

