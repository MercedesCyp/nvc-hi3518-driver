/*  static int32 sfdefFM1288_ReadData(mFM1288_ReadData* pReadData)
@introduction:
    读取FM1288寄存器的值

@parameter:
    iCmd
        指向读取数据结构体的指针

@return:
    void

*/
static int32 sfdefFM1288_ReadData(mFM1288_ReadData *pReadData)
{
	uint8 wBuf[5] = { 0 };
	uint8 rBuf[4] = { 0 };
	uint8 dataH = 0, dataL = 0;
	uint8 data = 0;
	int i = 0;
	int DataLength = 0;

	// Write the command to read data
	IIC_Start();
	IIC_WriteChar(DC_FM1288_ADDR_CHIP);
	if (IIC_ACK(DC_IIC_DETECTACK)) {
		NVCPrint("Write Device Address [DETECTACK] failed");
		return -1;
	}
	wBuf[0] = 0xfC;
	wBuf[1] = 0xf3;
	wBuf[2] = 0x37;
	wBuf[3] = pReadData->u8RegHigh;
	wBuf[4] = pReadData->u8RegLow;
	i = 0;
	DataLength = sizeof(wBuf);
	while (i < DataLength) {
		//NVCPrint("Send Data[%d]: 0x%02x", i, wBuf[i]);
		IIC_WriteChar(wBuf[i]);
		if (IIC_ACK(DC_IIC_DETECTACK)) {
			NVCPrint("Send Register Address [DETECTACK] failed");
			return -1;
		}
		i++;
	}
	IIC_Start();
	IIC_WriteChar(DC_FM1288_ADDR_CHIP + 1);
	if (IIC_ACK(DC_IIC_DETECTACK)) {
		NVCPrint("Write Device Address [DETECTACK] failed");
		return -1;
	}
	data = IIC_ReadChar();
	IIC_Stop();

	// Read higt byte data (register 0x26)
	IIC_Start();
	IIC_WriteChar(DC_FM1288_ADDR_CHIP);
	if (IIC_ACK(DC_IIC_DETECTACK)) {
		NVCPrint("Write Device Address [DETECTACK] failed");
		return -1;
	}
	rBuf[0] = 0xfc;
	rBuf[1] = 0xf3;
	rBuf[2] = 0x60;
	rBuf[3] = 0x26;
	i = 0;
	DataLength = sizeof(rBuf);
	while (i < DataLength) {
		//NVCPrint("Send Data[%d]: 0x%02x", i, rBuf[i]);
		IIC_WriteChar(rBuf[i]);
		if (IIC_ACK(DC_IIC_DETECTACK)) {
			NVCPrint("Send Data [DETECTACK] failed");
			return -1;
		}
		i++;
	}
	IIC_Start();
	IIC_WriteChar(DC_FM1288_ADDR_CHIP + 1);
	if (IIC_ACK(DC_IIC_DETECTACK)) {
		NVCPrint("Write Device Address [DETECTACK] failed");
		return -1;
	}
	dataH = IIC_ReadChar();
	NVCPrint("Read Data High: 0x%02x", dataH);
	IIC_Stop();

	// Read low byte data (register 0x25)
	IIC_Start();
	IIC_WriteChar(DC_FM1288_ADDR_CHIP);
	if (IIC_ACK(DC_IIC_DETECTACK)) {
		NVCPrint("Write Device Address [DETECTACK] failed");
		return -1;
	}
	rBuf[0] = 0xfc;
	rBuf[1] = 0xf3;
	rBuf[2] = 0x60;
	rBuf[3] = 0x25;
	i = 0;
	DataLength = sizeof(rBuf);
	while (i < DataLength) {
		//NVCPrint("Send Data[%d]: 0x%02x", i, rBuf[i]);
		IIC_WriteChar(rBuf[i]);
		if (IIC_ACK(DC_IIC_DETECTACK)) {
			NVCPrint("Send Data [DETECTACK] failed");
			return -1;
		}
		i++;
	}
	IIC_Start();
	IIC_WriteChar(DC_FM1288_ADDR_CHIP + 1);
	if (IIC_ACK(DC_IIC_DETECTACK)) {
		NVCPrint("Write Device Address [DETECTACK] failed");
		return -1;
	}
	dataL = IIC_ReadChar();
	NVCPrint("Read Data Low: 0x%02x", dataL);
	IIC_Stop();

	return (dataH << 8) + dataL;
}
